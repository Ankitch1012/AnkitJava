package Array;

public class InitialisationOfArray {
    public static void main(String[] args) {
        int[] arr = {1,2,3,4,5};
        int x = 2;
        System.out.println(x);
        System.out.println(arr[2]);
        arr[2] = 6;
        System.out.println(arr[2]);
        int[] ar = new int[6];
        System.out.println(ar[2]);
        ar[2] = 4;
        System.out.println(ar[2]);

    }
}
